#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*a;
	t_list	*tmp;

	a = (*lst);
	while (a != NULL)
	{
		tmp = a -> next;
		ft_lstdelone(a, del);
		a = tmp;
	}
	(*lst) = NULL;
}
