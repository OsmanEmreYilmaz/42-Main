#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int	strlen;

	strlen = ft_strlen(str);
	while (strlen >= 0)
	{
		if (str[strlen] == (char)c)
			return ((char *)&str[strlen]);
		strlen--;
	}
	return (NULL);
}
